<h2>Labels dialog</h2>

<img src="14.png">

User may configure labels view and behaviour through labels dialog.

The options the dialog provides are:
<ul>
	<li>Foreground color – the color the name of label is written.</li>
	<li>Background color – the background color the label.</li>
	<li>Mark to be deleted – user may want to delete label from the database.
		Label deletion requires the label to be unused:
		not assigned to an items, not used in a filter, not mentioned in list of auto-assigned labels in a source.
		Violation these restrictions cause a failure of the operation.</li>
	<li>Backup level – user may choose to back up current state of web-page at the moment this label is assigned.
		Two levels are available: backup plain HTML without external styles; embed external styles into backed up HTML page.</li>
	<li>Shortcut – user may assign shortcut which triggers assignment of label to selected item.
		If them item already contained the label, it will be removed.</li>
</ul>

