<h2>Filters</h2>

<img src="8.png">

Filters tab contains only a tree structure of all filters user has defined.
Additional icons are shown when hovering mouse cursor over particular object in tree.

<h3>Actions</h3>

If user clicks at a filter, the filter is applied to the list of items in the main content.
The detail of source can be shown in Filters dialog by clicking detail icon near the filter.

User may create a new filter by clicking on a corresponding icon near folder name.
The folder will be pre-filled in New filter dialog.
Similarly, if user clicks onto save icon, the currently applied filter will be pre-filled in New filter dialog.

User may clear currently applied filter by clicking on the brush icon or on All filters label.
Clicking on Ad-hoc label, ad-hoc dialog is shown.

<h3>Selected filter</h3>

Filter is highlighted by red color when it is recognized as currently applied filter.
Otherwise ad-hoc filter is highlighted instead.
