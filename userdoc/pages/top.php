<h2>Top panel</h2>

<h3>Current filter</h3>

<img src="4.png">

The top panel show overview of currently applied filter.
It also contains buttons which manipulate with this filter:
<ul>
	<li>Refresh – reapplies current filter.</li>
	<li>Edit filter – shows Ad-hoc dialog filled with current filter.</li>
	<li>Clear labels/all – if the filter contains labels query, the button clears it, keeping date range, read status criterion and sort order.
		Another click clears whole filter.</li>
</ul>

<h3>Items</h3>

<img src="5.png">

Top panel also contains two buttons which control items in main content.
<ul>
	<li># new items – shows number of available new items matching current filter which would be shown at the top.
		When the button is clicked, those items are added (shown) while maintaining current position in list.
		New items can also be added when user scrolls to the top of list.</li>
	<li>^ Mark read ^ – marks all items above currently selected one as read.</li>
</ul>
	
