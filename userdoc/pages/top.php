<h2>Top panel</h2>

<h3>Current filter</h3>

<img src="4.png">

The top panel show overview of currently applied filter.
It also contains buttons which manipulate with this filter:
<ul>
	<li>Refresh – reapplies current filter.</li>
	<li>Edit filter – shows the Ad-hoc dialog filled with the current filter.</li>
	<li>Clear labels/all – if the filter contains labels query, the button clears it, keeping date range, read status criterion and sort order.
		Another click clears the whole filter.</li>
</ul>

<h3>Items</h3>

<img src="5.png">

Top panel also contains two buttons which control items in the main content.
<ul>
	<li># new items – shows the number of available new items matching current filter which would be shown at the top.
		When the button is clicked, those items are added (shown) while maintaining the current position in the list.
		New items can also be added when the user scrolls to the top of the list.</li>
	<li>^ Mark read ^ – marks all items above currently selected one as read.</li>
</ul>
	
