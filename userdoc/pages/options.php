<h2>Options</h2>

<img src="6.png">

Options tab of the Left panel consists of three parts:
<ul>
	<li>Configuration of labels and shortcuts.</li>
	<li>Currently logged-in user and button to log out of the application.</li>
	<li>Link to this user documentation.</li>
</ul>

In the future, this tab might contain more application-global settings.
