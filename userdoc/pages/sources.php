<h2>Sources</h2>

<img src="7.png">

Sources tab contains only a tree structure of all sources the user has added.
Additional icons are shown when hovering mouse cursor over a particular object in the tree.

<h3>Actions</h3>

Only active sources are shown in default view; disabled sources can be shown by clicking on the icon of crumpled paper near All sources (click again to hide them).

If the user clicks on a source or folder of sources, the items of that source or all sources in the folder are shown in the main content area.
In fact a new temporary filter is created and applied.

The detail of a source can be shown in Source dialog by clicking on detail icon near the source.
If the source is the manual one, an icon which allows user to add a new manual item is shown (plus-signed-shaped icon).

The user may create a new source by clicking on a corresponding icon near folder name.
The folder will be pre-filled in New source dialog.

<h3>Selected sources</h3>

Sources are highlighted by red color when they are present in a currently applied filter.
If no source is in current filter, the label All sources is highlighted instead.

