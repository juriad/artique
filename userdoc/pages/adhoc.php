<h2>Ad-hoc dialog</h2>

<img src="12.png">

The user may alter or adjust currently applied filter via Ad-hoc dialog.
It is a simplified version of the Filter detail dialog which lacks persistent information.

User may set following criteria:
<ul>
	<li>Query consisting of labels combined with logical operators AND and OR.
		Remember that, each source has also a label with the same name.
		Operator AND (or no operator) has higher precedence than operator OR.
		No grouping by parentheses is supported.</li>
	<li>Date criteria (From, To) specify the date range of when the item must have been added to be matched.</li>
	<li>Read criterion â€“ whether to show read or unread items only.</li>
</ul>

The list of items matching the filter is always sorted either descending (default) or ascending by time of addition.
