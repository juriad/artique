<h2>Introduction</h2>

Artique is a web reader which allows users to watch new content on several internet sources.
User may organize the imported items; build his own library.

<h3>Sources</h3>

Those sources may provide standard RSS and Atom feeds.

Artique can even watch websites which does not have these feeds.
In that case, the reader parses the web-page and searches for changes in used-defined areas.
This way, it can detect changes in text content or track outgoing links.

The last available source is the user himself.
He can add his own items (articles) manually either using dialog in application or via an optional extension in common web browsers.

<h3>Library</h3>

Imported items may be organized by attaching labels to them.
When an item has some labels, it is searchable by filters.
User may define many filters which behave as a view or restriction of all items.

<h3>Other features</h3>

Artique has many other features.
It can:
<ul>
	<li>backup current web-page. It us useful in case the user may not afford to lose it.</li>
	<li>recommend sources. Sources are recommended by a user similarity algorithm which predicts what sources may the user like.</li>
	<li>export list of items. Any list of items defined by filter may be exported as a RSS or Atom feed.</li>
</ul>

<h3>No content</h3>

When the user loggs in the first time, he may be surprised by lack of any content in the application.
Therefore, it is desired to walk through this documentation at least briefly in order to understand principles of the application and configure it properly prior its usage.
The lack of content is intentional; advanced users are willing to configure the application in a way they like.
