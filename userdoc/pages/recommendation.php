<h2>Recommendation</h2>

<img src="16.png">

If user has chosen to add a recommended source, dialog is expanded sideways and list of recommended sources is shown.
Clicking onto the source, its detail informations are shown in the left part of dialog.

The rest of source creation process is the same as if the user had chosen custom source.

Recommendation is available only when user shares at least one source with at least one another user.
Recommendations are recalculated once a day, so it takes recommendation up to 24 hours to reflect changes of watched sources.
