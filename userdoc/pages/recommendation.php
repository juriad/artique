<h2>Recommendation</h2>

<img src="16.png">

If the user chooses to add a recommended source, the dialog is expanded sideways and list of recommended sources is shown.
Clicking onto the source, its detail information is shown in the left part of the dialog.

The rest of source creation process is the same as if the user chooses a custom source.

The Recommendation is available only if the user shares at least one source with at least one other user.
Recommendations are recalculated once a day, so it takes recommendation up to 24 hours to reflect changes of watched sources.
