<h2>New source</h2>

<img src="17.png">

When creating a new source, the user may choose whether he wants to create custom source by specifying the direct URL or by choosing one of recommended sources.

If the user chooses direct input of URL of a source, he must specify a valid URL address and a source type prior filling up other information.
Creation of Manual source is not supported, there may exist only one such source.
These two parameters cannot be changed later.

Once the user has filled this information, he shall click Fix URL and continue button.
The dialog will expand into the Source detail dialog.
