<h2>Overview</h2>

<img src="0.png">

Application user interface consists of four main areas:
<ul>
	<li>Left panel.
		Contains all control options: user may manipulate with sources, filters, labels through dialogs and trees shown in the left panel.
		The left panel is extended to show its whole content as soon as the user moves mouse cursor over it.
	</li>
	<li>Top panel.
		Shows an overview of currently applied filter and buttons which provide essential filter- and items-related functionality.</li>
	<li>Content.
		This is the most important area, it lists all items which match applied filter.</li>
	<li>Messages stack.
		Contains messages which inform the user about successful and failed  operations. 
		Messages stack is shown in column in the middle third of the content.</li>
</ul>
