<h2>Shortcuts dialog</h2>

<img src="15.png">

The user may create and manage his own set of shortcuts.

<h3>Types</h3>

Shortcuts are of three different types:
<ul>
	<li>Actions.
		Action shortcuts perform one of pre-defined actions.
		The list of supported action shortcuts can be found in Action shortcut dialog.</li>
	<li>Labels.
		Label shortcuts assign specified label to currently selected item; if the label is already present, it will be removed instead.</li>
	<li>Filters.
		Filter shortcuts changes currently applied filter to the specified one.</li>
</ul>

In the Shortcuts dialog, all existing shortcuts are shown: the key stroke they are activated by, their type and the action they perform or object they use.

<h3>Actions</h3>

The user may create a new action shortcut by clicking Add action shortcut button.
An Action shortcut dialog will be shown.

The other operation, the user may perform, is to delete any of existing shortcuts by selecting it and clicking on the Delete button.

<h3>Key stroke</h3>

Shortcuts are fired as soon as user presses appropriate key combination.
The combination may consists of virtually any key on keyboard combined with any of four modifiers (Control, Shift, Alt, Meta).

Key strokes require user to specify them in a particular format:
<ul>
	<li>Modifiers are specified as their first letter in Uppercase separated by a plus sign from the rest of key stroke.
		If there are no modifiers, the plus sign is optional.
		<br>
		Examples:
		<kbd>C+a</kbd>, <kbd>a</kbd>, <kbd>CA+a</kbd>
	</li>
	<li>The name of the key is letter, symbol, number or its name.
		<br>
		Examples:
		<kbd>a</kbd>, <kbd>=</kbd>, <kbd>1</kbd>, <kbd>tab</kbd>
	</li>
</ul>

